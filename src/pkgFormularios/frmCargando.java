    Timer fade = new Timer (1, new ActionListener () { 
    @Override
    public void actionPerformed(ActionEvent e) 
    { 
           float f = 0.0f;
        while(f < 1.0f){
            frmCargando.this.setOpacity(f);
            f += 0.000002f;            
        }
        fade.stop();
     } 
        });
    Timer fadeactual = new Timer (1, new ActionListener () { 
    @Override
    public void actionPerformed(ActionEvent e) 
    { 
           float f = 1.0f;
        while(f > 0.01f){
            frmCargando.this.setOpacity(f);
            f -= 0.000002f;            
        }
        fadeactual.stop();
        frmCargando.this.dispose();
        new frmLogin().show();
     } 
        });
    Timer duracion = new Timer (3000, new ActionListener () { 
    @Override
    public void actionPerformed(ActionEvent e) 
    { 
        fadeactual.start();
        duracion.stop();
     } 
        });
    public void TestearYComprobarTodo(){
        try{
        txt.setText("Configurando conexion con SQL Server..");
        H.ExecuteQuery("SELECT id_usuario FROM tbl.Usuarios");
        H.ExecuteQueryPSTM("SELECT id_usuario FROM tbl.Usuarios", new String[]{});
        txt.setText("Iniciando cypher, modo: AES 256 bytes..");
        H.AESenc("Prototype");
        txt.setText("Iniciando cypher, modo: RSA 1024 bytes..");
        H.RSAenc("Prototype");
        txt.setText("Terminando configuracion de cypher..");
        H.Encriptar("Prototype");
        txt.setText("Iniciando SCTB: Bancaria..");
        duracion.start();
        }catch(SQLException ex){
            H.Error(ex.toString());
        } catch (Exception ex) {
            H.Error(ex.toString());
        }
    }
    
