    private void btnSiguienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSiguienteActionPerformed
        try{
            getStrings();
            boolean r = false;
            if(chkCuarto.isSelected()){
                if(H.VerificarCampos(txt1) && H.VerificarCampos(txt2) && H.VerificarCampos(txt3) && H.VerificarCampos(txt4)){
                    if((Integer.parseInt(porce1.replace("%", "")) + Integer.parseInt(porce2.replace("%", "")) + Integer.parseInt(porce3.replace("%", "")) + Integer.parseInt(porce4.replace("%", ""))) == 100){
                        if(H.ExecuteCSTM("EXEC pcd.NuevoSeguro4Beneficiarios ?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?", new String[]{frmSeguros.id_prima, frmSeguros.nom_tipseguro, frmDatosdeTrabajo.id_cuenta, nomben1, apeben1, paren1, porce1, nomben2, apeben2, paren2, porce2, nomben3, apeben3, paren3, porce3, nomben4, apeben4, paren4, porce4})){
                            H.Message("Seguro correctamente agregado.");
                            r = true;
                        }
                    }else{
                        H.Warning("Porcentajes mal distribuidos.");
                    }
                }else{
                    H.Warning("Complete todos los campos.");
                }
            }else if(chkTercer.isSelected()){
                if(H.VerificarCampos(txt1) && H.VerificarCampos(txt2) && H.VerificarCampos(txt3)){
                    if((Integer.parseInt(porce1.replace("%", "")) + Integer.parseInt(porce2.replace("%", "")) + Integer.parseInt(porce3.replace("%", ""))) == 100){
                        if(H.ExecuteCSTM("EXEC pcd.NuevoSeguro3Beneficiarios ?,?,?,?,?,?,?,?,?,?,?,?,?,?,?", new String[]{frmSeguros.id_prima, frmSeguros.nom_tipseguro, frmDatosdeTrabajo.id_cuenta, nomben1, apeben1, paren1, porce1, nomben2, apeben2, paren2, porce2, nomben3, apeben3, paren3, porce3})){
                            H.Message("Seguro correctamente agregado.");
                            r = true;
                        }
                    }else{
                        H.Warning("Porcentajes mal distribuidos.");
                    }
                }else{
                    H.Warning("Complete todos los campos.");
                }
            }else if(chkSegundo.isSelected()){
                if(H.VerificarCampos(txt1) && H.VerificarCampos(txt2)){
                    if((Integer.parseInt(porce1.replace("%", "")) + Integer.parseInt(porce2.replace("%", ""))) == 100){
                        if(H.ExecuteCSTM("EXEC pcd.NuevoSeguro2Beneficiarios ?,?,?,?,?,?,?,?,?,?,?", new String[]{frmSeguros.id_prima, frmSeguros.nom_tipseguro, frmDatosdeTrabajo.id_cuenta, nomben1, apeben1, paren1, porce1, nomben2, apeben2, paren2, porce2})){
                            H.Message("Seguro correctamente agregado.");
                            r = true;
                        }
                    }else{
                        H.Warning("Porcentajes mal distribuidos.");
                    }
                }else{
                    H.Warning("Complete todos los campos.");
                }
            }else if(chkPrimer.isSelected()){
                if(H.VerificarCampos(txt1)){
                    if(Integer.parseInt(porce1.replace("%", "")) == 100){
                        if(H.ExecuteCSTM("EXEC pcd.NuevoSeguro1Beneficiario ?,?,?,?,?,?,?", new String[]{frmSeguros.id_prima, frmSeguros.nom_tipseguro, frmDatosdeTrabajo.id_cuenta, nomben1, apeben1, paren1, porce1})){
                            H.Message("Seguro correctamente agregado.");
                            r = true;
                        }
                    }else{
                        H.Warning("Porcentajes mal distribuidos.");
                    }
                }else{
                    H.Warning("Complete todos los campos.");
                }
            }
            if(r){
                H.showWithTransition(this, new frmTipodeCuenta());
            }
        }catch(SQLException ex){
            H.Error(ex.toString());
        }
    }//GEN-LAST:event_btnSiguienteActionPerformed

