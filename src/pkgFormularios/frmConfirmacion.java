    Timer fade;
    
    public void closeThis(){
    fade = new Timer (1, new ActionListener () { 
    @Override
    public void actionPerformed(ActionEvent e) 
    { 
           float f = 1.0f;
        while(f > 0.01f){
            frmConfirmacion.this.setOpacity(f);
            f -= 0.00001f;            
        }
        frmConfirmacion.this.dispose();
        fade.stop();        
     } 
        });
    fade.start();
    }
    
    Timer fade2 = new Timer (1, new ActionListener () { 
    @Override
    public void actionPerformed(ActionEvent e) 
    { 
           float f = 0.0f;
        while(f < 1.0f){
            frmConfirmacion.this.setOpacity(f);
            f += 0.00001f;            
        }
        fade2.stop();
     } 
        });
        
