    public void cleanStrings(){
        try {
            String[] temp =  new String[]{frmPersonas.nom_persona, frmPersonas.ape_persona, frmPersonas.correo_persona, frmDatosdePersona.dui_datpersona, frmDatosdePersona.nit_datpersona, frmDatosdePersona.dir_datpersona, frmDatosdePersona.telcel_datpersona, frmDatosdePersona.telcasa_datpersona, frmDatosdePersona.nom_genero, frmDatosdePersona.nom_estcivil, salario_dattrabajo, nom_datempresa, nom_cargotrabajo};
            for(int i = 0; i < temp.length; i++){
                temp[i] = null;
            }
        } catch (Exception ex) {
            H.Error(ex.toString());
        }
    }
