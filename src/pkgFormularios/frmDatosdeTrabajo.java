    private void btnAplicarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAplicarActionPerformed
        try{
            getStrings();
            if(H.VerificarCampos(new Object[]{txtSalario})){
                int salario = Integer.parseInt(salario_dattrabajo);
                if(salario > 0 && salario < 30000){
                    if(H.ExecuteCSTM("EXEC pcd.AgregarCliente ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?", new String[]{frmPersonas.nom_persona, frmPersonas.ape_persona, frmPersonas.correo_persona, String.valueOf(H.id_usuario), H.Encriptar(frmDatosdePersona.dui_datpersona), H.Encriptar(frmDatosdePersona.nit_datpersona), frmDatosdePersona.dir_datpersona, frmDatosdePersona.telcel_datpersona, frmDatosdePersona.telcasa_datpersona, frmDatosdePersona.nom_genero, frmDatosdePersona.nom_estcivil, salario_dattrabajo, nom_datempresa, nom_cargotrabajo})){
                        String id_per = String.valueOf(H.SearchOne("SELECT id_persona FROM tbl.Personas WHERE correo_persona = '"+frmPersonas.correo_persona+"'"));
                        if(H.ExecuteCSTM("EXEC pcd.NuevaCuenta ?, ?, ?, 'Corriente', 'Activa'", new String[]{frmPersonas.correo_persona, H.Encriptar(id_per), H.Encriptar("0000")})){
                            id_cuenta = String.valueOf(H.SearchOne("SELECT id_cuenta FROM tbl.Cuentas WHERE id_persona = "+id_per));
                            H.showWithTransition(this, new frmSeguros());
                        }
                    }
                }else{
                    H.Warning("Salario invalido");
                }
            }else{
                H.Warning("Salario vacio.");
            }
        }catch(Exception ex){
            H.Error(ex.toString());
        }
    }//GEN-LAST:event_btnAplicarActionPerformed

